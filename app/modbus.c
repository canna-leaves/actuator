/**
 * @brief modbus-rtu请求协议打包（写多寄存器）
 * @param[in] slave 从机地址
 * @param[in] addr 寄存器起始地址
 * @param[in] v_len 数据长度
 * @param[out] out_buf 打包数据输出
 * @param[out] out_len 打包数据长度
 * @retval RTU_REQ 成功
 */
u8 modbus_write_registers_pack(u8 slave, u16 addr, u16 v_len,
                               u8 *out_buf, u16 *out_len) {
  out_buf[0] = slave;
  out_buf[1] = 0x10;
  out_buf[2] = addr>>8;
  out_buf[3] = addr&0xFF;
  out_buf[4] = v_len>>8;
  out_buf[5] = v_len&0xFF;
  u8 blen = v_len<<1;
  out_buf[6] = blen;
  u16 crc_0 = usMBCRC16(out_buf, 7 + blen);
  out_buf[7 + blen] = crc_0&0xFF;
  out_buf[7 + blen + 1] = crc_0>>8;
  *out_len = 7 + blen + 2;
  return RTU_REQ;
}

/**
 * @brief modbus-rtu协议(回复处理) 
 * @param[in] in_buf 输入数据
 * @param[in] in_len 数据长度
 * @param[in] req_buf 请求数据
 * @param[out] out_buf 寄存器指针
 * @param[out] out_len 寄存器数量
 * @retval RTU_REQ 成功
 */
u8 modbus_rep_handler(u8 *in_buf, u16 in_len, u8 *req_buf, 
                      u8 **out_buf, u16 *out_len) {
  if(in_buf[0] != req_buf[0] || in_buf[1] != req_buf[1]) {
    return RTU_ERR;
  }
  if(0x03 == in_buf[1] 
     && in_len == (5 + 2 * (req_buf[4]<<8 | req_buf[5])) 
     && in_buf[2] == 2 * (req_buf[4]<<8 | req_buf[5])){
    *out_buf = in_buf + 3;
    *out_len = 2 * (req_buf[4]<<8 | req_buf[5]);
    return RTU_REP;
  } else if(0x04 == in_buf[1] 
     && in_len == (5 + 2 * (req_buf[4]<<8 | req_buf[5])) 
     && in_buf[2] == 2 * (req_buf[4]<<8 | req_buf[5])){
    *out_buf = in_buf + 3;
    *out_len = 2 * (req_buf[4]<<8 | req_buf[5]);
    return RTU_REP;
  } else if(0x06 == in_buf[1] && 8 == in_len) {
    if(in_buf[2] == req_buf[2] 
       && in_buf[3] == req_buf[3]
       && in_buf[4] == req_buf[4]
       && in_buf[5] == req_buf[5]) {
         *out_buf = NULL;
         *out_len = 0;
         return RTU_REP;
       }
  } else if(0x10 == in_buf[1] && 8 == in_len) {
    if(in_buf[2] == req_buf[2] 
       && in_buf[3] == req_buf[3]
       && in_buf[4] == req_buf[4]
       && in_buf[5] == req_buf[5]) {
         *out_buf = NULL;
         *out_len = 0;
         return RTU_REP;
       }
  }
  return RTU_ERR;
}

/**
 * @brief modbus-rtu协议处理
 * @param[in] in_buf 输入数据
 * @param[in] in_len 数据长度
 * @param[out] out_buf 寄存器指针
 * @param[out] out_len 寄存器数量
 * @retval RTU_REQ 请求
 * @retval RTU_ERR 解析错误
 * @retval RTU_REP 回复
 */
u8 modbus_handler(u8 slave, u8 master, u8 *in_buf, u16 in_len, u8 *out_buf, u16 *out_len) {
  if((in_len >= 4)
     && (0 == usMBCRC16(( UCHAR * )in_buf, in_len)))
  {
//    USHORT sum0 = usMBCRC16(( UCHAR * )in_buf, in_len);
//    if(0 != sum0) {
//      return RTU_ERR;
//    }
    if(in_buf[0] < 0x01 || in_buf[0] > 0x99) {
      return RTU_ERR;
    }
    
    if(COM_MODE_MASTER == master) {
      return RTU_REP;
    } else if(slave != in_buf[0]) {
      return RTU_SLAVE_ERR;
    }
    
    if(0x03 != in_buf[1] && 0x06 != in_buf[1] && 0x10 != in_buf[1] && 0x04 != in_buf[1]) {
      return RTU_ERR;
    }
    u16 addr = ((in_buf[2] << 8) | in_buf[3]);
    out_buf[0] = in_buf[0];
    out_buf[1] = in_buf[1];
    
    if(0x03 == in_buf[1] || 0x04 == in_buf[1]) {
      u16 len = ((in_buf[4] << 8) | in_buf[5]);
      if(len < 0x01 || len > 0x7d) {
        return RTU_ERR;
      }
      out_buf[2] = len<<1;
      *out_len = 3 + out_buf[2] + 2;
      if(FALSE == read_registers(in_buf[0], addr, len, out_buf + 3)){
        return RTU_ERR;
      } else {
        u16 crc_0 = usMBCRC16(out_buf, 3 + out_buf[2]);
        out_buf[3 + out_buf[2]] = crc_0&0xFF;
        out_buf[3 + out_buf[2] + 1] = crc_0>>8;
        return RTU_REQ;
      }
    } else if(0x06 == in_buf[1]) {
      out_buf[2] = in_buf[2];
      out_buf[3] = in_buf[3];
      out_buf[4] = in_buf[4];
      out_buf[5] = in_buf[5];
      out_buf[6] = in_buf[6];
      out_buf[7] = in_buf[7];
      *out_len = 8;
      if(FALSE == write_registers(in_buf[0], addr, 1, in_buf + 4)){
        return RTU_ERR;
      } else {
        return RTU_REQ;
      }
    } else if(0x10 == in_buf[1]) {
      u16 len = ((in_buf[4] << 8) | in_buf[5]);
      if(len < 0x01 || len > 0x7d) {
        return RTU_ERR;
      }
      *out_len = 8;
      if(FALSE == write_registers(in_buf[0], addr, len, in_buf + 7)){
        return RTU_ERR;
      } else {
        out_buf[2] = in_buf[2];
        out_buf[3] = in_buf[3];
        out_buf[4] = in_buf[4];
        out_buf[5] = in_buf[5];
        u16 crc_0 = usMBCRC16(out_buf, 6);
        out_buf[6] = crc_0&0xFF;
        out_buf[7] = crc_0>>8;
        return RTU_REQ;
      }
    }
  }
  return RTU_ERR;
}