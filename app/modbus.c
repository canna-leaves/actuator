/**
 * @brief crc16校验
 * @param[in] pucFrame 数据
 * @param[in] usLen 数据长度
 * @retval 校验值
 */
USHORT
usMBCRC16( UCHAR * pucFrame, USHORT usLen )
{
  UCHAR           ucCRCHi = 0xFF;
  UCHAR           ucCRCLo = 0xFF;
  int             iIndex;
  
  while( usLen-- )
  {
    iIndex = ucCRCLo ^ *( pucFrame++ );
    ucCRCLo = ( UCHAR )( ucCRCHi ^ aucCRCHi[iIndex] );
    ucCRCHi = aucCRCLo[iIndex];
  }
  return ( USHORT )( ucCRCHi << 8 | ucCRCLo );
}

/**
 * @brief modbus-rtu请求协议打包（读多寄存器）
 * @param[in] slave 从机地址
 * @param[in] addr 寄存器起始地址
 * @param[in] len 数据长度
 * @param[out] out_buf 打包数据输出
 * @param[out] out_len 打包数据长度
 * @retval RTU_REQ 成功
 */
u8 modbus_read_registers_pack(u8 slave, u16 addr, u16 len, 
                              u8 *out_buf, u16 *out_len) {
  out_buf[0] = slave;
  out_buf[1] = 0x03;
  out_buf[2] = addr>>8;
  out_buf[3] = addr&0xFF;
  out_buf[4] = len>>8;
  out_buf[5] = len&0xFF;
  u16 crc_0 = usMBCRC16(out_buf, 6);
  out_buf[6] = crc_0&0xFF;
  out_buf[7] = crc_0>>8;
  *out_len = 8;
  return RTU_REQ;
}

